            <h2 class="font-gold">Відповіді на поширені запитання про латинку</h2>
            <hr>
            <div>
                <ul>
                    <li>
                        <span>Ми втратимо свою історію, якщо перейдемо на латинку?</span>
                        <p>Це дуже маніпулятивне запитання, адже одне не виключає іншого. Не можна відмовитися від історії, змінивши абетку. Ось ідентичне маніпулятивне запитання: якщо в нас народиться друга дитина, то чи зникне перша? Очевидно, що ні. Навіть якщо у вас змінився партнер, обидві дитини однаково ваші. Це все — ваше минуле.

                        Історію неможливо видалити, запровадивши нову абетку. Абетка — це лише одяг для будь-якої мови. Якщо ви змінюєте одяг, ви залишаєтеся тою самою людиною з тією ж історією. Змінюється за такої умови тільки ваш зовнішній вигляд, тобто те, як вас сприймають інші.

                        Хорвати, наприклад, користувалися власною версією глаголиці, але перейшли на латинку. Тепер вони встановлюють пам’ятники літерам із глаголиці та продають свою «квадратну» глаголицю як сувеніри. Їхня історія залишилася їхньою історією. І таких прикладів дуже багато. Норвежці, шведи й данці нині пишуть латинкою, але руни є їхньою історією. Вона нікуди не зникла й не стерлася зі зміною абетки. Аналогічно й петрогліфи Кам’яної могили нині є українською історією незалежно від того, якою абеткою ми з вами тепер пишемо.</p>
                    </li>
                    <li>
                        <span>Ми втратимо свою культуру, адже вся українська література написана кирилицею?</span>
                        <p>Ми не втратимо своєї культури після переходу на латинку, як і нашої історії: це неможливо. Усі потрібні нам у майбутньому кириличні тексти легко адаптуються за допомогою штучного інтелекту вже сьогодні. Їх не треба переписувати чи передруковувати вручну. Приклад такого «автоматичного перекладача» вже працює на цьому сайті. До того ж тексти українських класиків уже давно адаптують до чинної версії української кирилиці. Ми не читаємо їх в оригіналі. Котляревський, Сковорода, Франко та Шевченко не писали на 100 % так, як ми читаємо їх тепер. Тож нічого нового: ми як адаптували твори наших класиків до чинної абетки та правопису, так і адаптуватимемо, але робитимемо це не кирилицею, а латинкою.</p>
                    </li>
                    <li>
                        <span>Наші діти не зможуть прочитати графіті на стінах Софії Київської?</span>
                        <p>Це теж маніпуляція, тому що без фахової освіти їх не зможете прочитати й ви. Однак ніхто не забороняє нам запровадити в усіх школах факультатив із вивчення давньоукраїнської кирилиці для дітей 5–6 класів і заразом звозити їх у собор, щоби почитати графіті на стінах.</p>
                    </li>
                    <li>
                        <span>Кирилиця — ідеальна абетка для української мови?</span>
                        <p>На жаль, це не так. По-перше, не всі звуки в ній подані логічно. Наприклад, літери Я, Ю, Є та Ї позначають два звуки або один із пом’якшенням попереднього приголосного, що можна записати відповідно літерами ЙА/ЬА, ЙУ/ЬУ, ЙЕ/ЬЕ, ЙІ/ЬІ, а про літеру Ё в українській кирилиці забули. І ми чомусь пишемо ЙО або ЬО замість Ё. Ми звикли писати цього, його тощо, а логічно було б писати цёго та ёго. Ми ж не пишемо цьа та йама замість ця та яма, правда?
                        По-друге, кирилиця важче читається, адже має багато схожих «квадратних» літер, які складніше розрізняти.
                        По-третє, кириличні тексти займають більше місця, ніж написані латинкою, що збільшує витрати на друк.</p>
                    </li>
                    <li>
                        <span>Чому українці мають змінювати абетку й підлаштовуватися під іноземців?</span>
                        <p>Не варто називати перехід на латинку підлаштовуванням під потреби іноземців, адже такий перехід — це насамперед інвестиція в інтересах українців. Більше можна прочитати в розділі «Для чого українцям латинка?».</p>
                    </li>
                    <li>
                        <span>Україна — православна країна, а всі православні пишуть кирилицею?</span>
                        <p>Переважна більшість українців дійсно православна. Ми переконані, що Православна церква України підтримає осучаснення української абетки. Наприклад, мусульмани в Туреччині пишуть латинкою, так само як і мешканці Ватикану. Це нікому з них не заважає.</p>
                    </li>
                    <li>
                        <span>Треба зробити англійську другою державною, а не переходити на латинку?</span>
                        <p>Державний статус англійської мови, як і збільшення кількості англомовного вмісту в Україні, загрожує існуванню української мови. Наочним прикладом тут може бути історія зникнення ірландської мови в Ірландії. А от перехід на латинку — це лише зміна абетки (зовнішньої форми), яка, навпаки, дасть поштовх до розвитку та осучаснення української мови.</p>
                    </li>
                    <li>
                        <span>Латинка змінить українську мову?</span>
                        <p>Проєкт української латинки Максима Прудеуса не змінює вимови українських слів. Абетку розроблено відповідно до мовних традицій,а не навпаки. Поряд із «правилом дев’ятки» абетка закріплює твердість приголосних у деяких словах іншомовного походження.</p>
                    </li>
                    <li>
                        <span>Немає прикладів, коли перехід на латинку позитивно вплинув на розвиток країни?</span>
                        <p>Швеція, Данія, Норвегія, Туреччина, Хорватія та багато інших країн відмовилися від застарілих абеток на користь латинки.</p>
                    </li>
                    <li>
                        <span>Перехід на латинку потребує багато витрат?</span>
                        <p>Витрати — це те, що не окуповується та не дає прибутку. Тому перехід на латинку — це не витрати. Це радше внесок у майбутнє, який не лише убезпечить українців від розчинення в інших етносах, а й принесе фінансовий зиск від туризму та іноземних інвестицій в економіку України. До того ж перехід на латинку необов’язково оплачувати тільки українцям. Наші європейські партнери своїми грантами можуть допомогти нам здійснити такий перехід, адже це в наших спільних інтересах.</p>
                    </li>
                    <li>
                        <span>Вчитися писати й читати латинкою дуже довго?</span>
                        <p>Переважна більшість глядачів після перегляду сорокахвилинного <a href="https://www.youtube.com/watch?v=nHeE2x2UNw4">відео Максима Прудеуса на ютубі</a> пише латинкою без помилок. Отже, потрібна лише одна година, щоби перейти на латинку: сорок хвилин на перегляд відео і двадцять хвилин на практику.</p>
                    </li>
                    <li>
                        <span>Зрілому поколінню буде дуже важко?</span>
                        <p>Наприкінці ХХ століття українці писали смс переважно латинкою, тому для зрілого покоління такий перехід не буде складним. Це підтверджують і коментарі від літніх людей під <a href="https://www.youtube.com/watch?v=nHeE2x2UNw4">відео Максима на ютубі</a></p>
                    </li>
                    <li>
                        <span>Чим погана офіційна транслітерація, як у паспорті?</span>
                        <p>По-перше, її неправильно читають. Oleh читають як Олех або взагалі як Олей. Слово holod — це холод чи голод? А holodomor як прочитати? Це риторичні запитання щодо проблем «паспортної» транслітерації. По-друге, офіційна «паспортна» транслітерація робить слова надзвичайно довгими.
                        Так, слово борщ перетворюється на borshch, а якщо — на yakshcho. Проєкт української латинки Максима Прудеуса розв’язує ці проблеми.</p>
