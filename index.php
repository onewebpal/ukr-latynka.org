                        <span>Іноземні інвестиції</span>
                        <p>Латинка полегшить вивчення української мови іноземцями, що за сприятливих умов для ведення бізнесу збільшить іноземні інвестиції в українську економіку. Ми станемо зрозуміліші цивілізованим країнам, адже латинські літери вміє читати вся планета.</p>
                    </li>
                    <li>
                        <span>Порозуміння з іншими слов’янами</span>
                        <p>Поляки, чехи, словаки, словенці, македонці та хорвати зможуть читати та розуміти українські тексти, як-от наші новини, жарти або українську історію, що швидко зблизить нас і зробить «своїми» в європейській слов’янській спільноті.</p>
